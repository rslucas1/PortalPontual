package br.com.lucasdev.modelo.relatorios;

public class DescontoFinanceiro {
	
	private String numPedido, vlTotalNota, vlDescFin, percDescFinan, numNota, emissaoNota, nomeCliente;

	public String getNumPedido() {
		return numPedido;
	}

	public void setNumPedido(String numPedido) {
		this.numPedido = numPedido;
	}

	public String getVlTotalNota() {
		return vlTotalNota;
	}

	public void setVlTotalNota(String vlTotalNota) {
		this.vlTotalNota = vlTotalNota;
	}

	public String getVlDescFin() {
		return vlDescFin;
	}

	public void setVlDescFin(String vlDescFin) {
		this.vlDescFin = vlDescFin;
	}

	public String getPercDescFinan() {
		return percDescFinan;
	}

	public void setPercDescFinan(String percDescFinan) {
		this.percDescFinan = percDescFinan;
	}

	public String getNumNota() {
		return numNota;
	}

	public void setNumNota(String numNota) {
		this.numNota = numNota;
	}


	public String getEmissaoNota() {
		return emissaoNota;
	}

	public void setEmissaoNota(String emissaoNota) {
		this.emissaoNota = emissaoNota;
	}

	public String getNomeCliente() {
		return nomeCliente;
	}

	public void setNomeCliente(String nomeCliente) {
		this.nomeCliente = nomeCliente;
	}
	
	
}
