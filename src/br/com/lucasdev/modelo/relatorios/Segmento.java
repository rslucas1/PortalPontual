package br.com.lucasdev.modelo.relatorios;

public class Segmento {
	private String cod_segmento;
	private String desc_segmento;
	private boolean ativo;
	public String getCod_segmento() {
		return cod_segmento;
	}
	public void setCod_segmento(String cod_segmento) {
		this.cod_segmento = cod_segmento;
	}
	public String getDesc_segmento() {
		return desc_segmento;
	}
	public void setDesc_segmento(String desc_segmento) {
		this.desc_segmento = desc_segmento;
	}
	public boolean isAtivo() {
		return ativo;
	}
	public void setAtivo(boolean ativo) {
		this.ativo = ativo;
	}
	
	
	
	
}
